# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada
n = float(input('Informe um número: '))
dobro = n * 2
triplo = n * 3
raiz = n**(1/2)
print(f'O número {n}:\nSeu dobro é {dobro:.2f}\nSeu triplo é {triplo:.2f}\nSua raiz quadrada é {raiz:.2f}')
